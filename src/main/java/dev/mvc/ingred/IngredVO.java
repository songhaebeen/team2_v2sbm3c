package dev.mvc.ingred;

public class IngredVO {
	private int ingredno;
	private String ingredname;
	private String ingredeffect;
	public int getIngredno() {
		return ingredno;
	}
	public void setIngredno(int ingredno) {
		this.ingredno = ingredno;
	}
	public String getIngredname() {
		return ingredname;
	}
	public void setIngredname(String ingredname) {
		this.ingredname = ingredname;
	}
	public String getIngredeffect() {
		return ingredeffect;
	}
	public void setIngredeffect(String ingredeffect) {
		this.ingredeffect = ingredeffect;
	}
}
